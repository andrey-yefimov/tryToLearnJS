					function contribution() {
					    var summ = parseFloat(prompt("Вас приветствует программа по расчету депозита. Введите сумму, которую вы хотели бы положить на счет:"));
					    var percent = parseFloat(prompt("Отлично, теперь введите годовой процент, на который вы вкладываете депозит:"));
					    var years = parseFloat(prompt("А теперь введите количество лет, на которое вы хотите вложить сумму:"));
					    for (count = 1; count <= years; count++) {
					        summ = summ + ((summ / 100) * percent);
					        alert("Ваш вклад на " + count + " год составит: " + summ);
					    }
					    alert("Спасибо за использование программы!");
					}
