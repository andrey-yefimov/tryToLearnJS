					function number() {
					    var answer = parseInt(Math.random() * 100);
					    while (true) {
					        var user_answer = prompt("Введите число от 0 до 100. Для выхода из игры введите q");
					        if (user_answer == "q") {
					            alert("Спасибо за игру! Всего вам наилучшего!");
					            break;
					        }
					        if (user_answer > answer) {
					            alert("Ваш ответ слишком большой!");
					        } else if (user_answer < answer) {
					            alert("Ваш ответ слишком маленький!");
					        } else if (user_answer == answer) {
					            alert("Вы угадали! Поздравляем!");
					            break;
					        } else {
					            alert("Похоже, что вы ввели что-то не то...");
					        }
					    }
					}

					function artillery() {
					    var counter = 0;
					    var answer = parseInt(prompt("Есть две цели, которые лежат в пределах от 0 до 90 градусов. Введите угол, на который следует повернуть орудие и произвести выстрел."));
					    if (answer == NaN || answer < 0 || answer > 90) {
					        alert("Нужно ввести число от 0 до 90, иначе орудие не сможет работать!");
					    } else {
					        if ((answer >= 15) && (answer <= 30)) {
					            alert("Отличный выстрел, одна цель есть!");
					            counter++;
					        } else {
					            alert("Упс, кажется промазали!");
					        }
					    }
					    var answer = prompt("Попробуйте поразить вторую цель!");
					    if (answer == NaN || answer < 0 || answer > 90) {
					        alert("Нужно ввести число от 0 до 90, иначе орудие не сможет работать!");
					    } else {
					        if ((answer >= 45) && (answer <= 60)) {
					            alert("Отличный выстрел, цель поражена!");
					            counter++;
					        } else {
					            alert("Упс, кажется промазали!");

					        }
					    }
					    alert("Итого поражено целей: " + counter);
					}

